#!/usr/bin/env python3
"""
function add with two floats
"""


def add(a: float, b: float) -> float:
    """
    Adds two floating-point numbers.

    Args:
        a (float): The first operand.
        b (float): The second operand.

    Returns:
        float: The sum of the two operands.
    """
    return a + b
